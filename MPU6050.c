    if(Data == 0x68)
    {
       // printf("I2C Read Test Passed, MPU6050 Address: 0x%x\r\n", Data);
       cputs("I2C Read Test Passed, MPU6050 Address: 0x68\r\n");       
 return(1);   
   }
     //printf("ERROR: I2C Read Test Failed, Stopping\r\n");
   cputs("ERROR: I2C Read Test Failed, Stopping\r\n");
 
